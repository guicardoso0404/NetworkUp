// Configura√ß√µes da API1
const API_BASE_URL = 'http://localhost:3002/api';

document.addEventListener('DOMContentLoaded', function() {
    // FOR√áAR LIMPEZA COMPLETA PARA DEBUG
    console.log('üßπ Limpando localStorage para debug...');
    localStorage.clear();
    sessionStorage.clear();
    
    // Aguardar um pouco antes de verificar usu√°rio
    setTimeout(() => {
        // Verificar se usu√°rio j√° est√° logado
        const currentUser = getCurrentUser();
        if (currentUser) {
            console.log('‚úÖ Usu√°rio j√° logado, redirecionando...');
            window.location.href = '/feed';
            return;
        }
        
        console.log('üìù Usu√°rio n√£o logado, carregando p√°gina de cadastro');
    }, 100);
    
    // Configurar formul√°rio de cadastro
    const cadastroForm = document.getElementById('registerForm');
    if (cadastroForm) {
        cadastroForm.addEventListener('submit', handleRegister);
        
        // Configurar valida√ß√£o em tempo real
        setupFormValidation();
    }
    
    // Configurar toggle de senha
    const toggleBtns = document.querySelectorAll('.toggle-password');
    toggleBtns.forEach(btn => {
        btn.addEventListener('click', function() {
            const input = this.parentElement.querySelector('input');
            togglePassword(input.id);
        });
    });
});

// Fun√ß√£o de cadastro
async function handleRegister(event) {
    event.preventDefault();
    
    const form = event.target;
    const formData = new FormData(form);
    const data = Object.fromEntries(formData.entries());
    const submitButton = form.querySelector('button[type="submit"]');
    
    // Valida√ß√µes
    if (!data.nome || !data.email || !data.senha) {
        showToast('Por favor, preencha todos os campos obrigat√≥rios', 'error');
        return;
    }
    
    if (!validateEmail(data.email)) {
        showToast('E-mail inv√°lido', 'error');
        return;
    }
    
    if (!validatePassword(data.senha)) {
        showToast('A senha deve ter pelo menos 6 caracteres', 'error');
        return;
    }
    
    if (data.confirmSenha && data.senha !== data.confirmSenha) {
        showToast('As senhas n√£o coincidem', 'error');
        return;
    }
    
    const termsCheckbox = form.querySelector('input[name="terms"]');
    if (termsCheckbox && !termsCheckbox.checked) {
        showToast('Voc√™ deve aceitar os termos de uso', 'error');
        return;
    }
    
    setButtonLoading(submitButton, true);
    
    try {
        console.log('üìù Tentando cadastro:', data.email);
        
        const response = await fetch(`${API_BASE_URL}/auth/cadastro`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify(data)
        });
        
        const result = await response.json();
        
        if (result.success) {
            console.log('‚úÖ Cadastro realizado com sucesso!');
            console.log('üë§ Dados do usu√°rio cadastrado:', result.data);
            showToast('Cadastro realizado com sucesso!', 'success');
            
            // Fazer login autom√°tico ap√≥s cadastro
            console.log('üîê Fazendo login autom√°tico...');
            
            try {
                const loginResponse = await fetch(`${API_BASE_URL}/auth/login`, {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json'
                    },
                    body: JSON.stringify({
                        email: data.email,
                        senha: data.senha
                    })
                });
                
                const loginResult = await loginResponse.json();
                
                if (loginResult.success) {
                    console.log('‚úÖ Login autom√°tico realizado!');
                    console.log('üë§ Usu√°rio logado:', loginResult.data.usuario);
                    
                    // Salvar dados do usu√°rio no localStorage
                    localStorage.setItem('currentUser', JSON.stringify(loginResult.data.usuario));
                    
                    showToast('Bem-vindo! Redirecionando para o feed...', 'success');
                    
                    // Redirecionar para o feed imediatamente
                    setTimeout(() => {
                        console.log('üîÑ Redirecionando para o feed...');
                        window.location.href = '/feed';
                    }, 1000);
                } else {
                    console.log('‚ö†Ô∏è Login autom√°tico falhou:', loginResult.message);
                    showToast('Cadastro realizado! Redirecionando para o login...', 'success');
                    setTimeout(() => {
                        window.location.href = '/login';
                    }, 1500);
                }
            } catch (loginError) {
                console.error('‚ùå Erro no login autom√°tico:', loginError);
                showToast('Cadastro realizado! Redirecionando para o login...', 'success');
                setTimeout(() => {
                    window.location.href = '/login';
                }, 1500);
            }
        } else {
            console.log('‚ùå Cadastro falhou:', result.message);
            showToast(result.message || 'Erro no cadastro', 'error');
        }
    } catch (error) {
        console.error('‚ùå Erro no cadastro:', error);
        showToast('Erro de conex√£o. Verifique se o servidor est√° rodando.', 'error');
    } finally {
        setButtonLoading(submitButton, false);
    }
}

// Fun√ß√£o para obter usu√°rio atual
function getCurrentUser() {
    try {
        const userStr = localStorage.getItem('currentUser');
        return userStr ? JSON.parse(userStr) : null;
    } catch (error) {
        console.error('Erro ao recuperar usu√°rio:', error);
        localStorage.removeItem('currentUser');
        return null;
    }
}

// Valida√ß√£o de email
function validateEmail(email) {
    const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return re.test(email);
}

// Valida√ß√£o de senha
function validatePassword(password) {
    return password.length >= 6;
}

// Toggle senha
function togglePassword(inputId) {
    const input = document.getElementById(inputId);
    const button = input.parentElement.querySelector('.toggle-password');
    
    if (input && button) {
        if (input.type === 'password') {
            input.type = 'text';
            button.textContent = 'üôà';
            button.setAttribute('title', 'Ocultar senha');
        } else {
            input.type = 'password';
            button.textContent = 'üëÅÔ∏è';
            button.setAttribute('title', 'Mostrar senha');
        }
    }
}

// Fun√ß√£o global para ser chamada pelo HTML
window.togglePassword = togglePassword;

// Loading button
function setButtonLoading(button, loading = true) {
    if (loading) {
        button.classList.add('loading');
        button.disabled = true;
        button.textContent = 'Cadastrando...';
    } else {
        button.classList.remove('loading');
        button.disabled = false;
        button.textContent = 'Cadastrar';
    }
}

// Configurar valida√ß√£o de formul√°rio
function setupFormValidation() {
    const inputs = document.querySelectorAll('input[required]');
    
    inputs.forEach(input => {
        input.addEventListener('blur', () => validateField(input));
        input.addEventListener('input', () => clearFieldError(input));
    });
}

// Validar campo individual
function validateField(input) {
    const formGroup = input.closest('.form-group');
    let isValid = true;
    let message = '';
    
    // Valida√ß√µes espec√≠ficas
    if (input.type === 'email' && input.value) {
        if (!validateEmail(input.value)) {
            isValid = false;
            message = 'E-mail inv√°lido';
        }
    } else if (input.type === 'password' && input.value) {
        if (!validatePassword(input.value)) {
            isValid = false;
            message = 'Senha deve ter pelo menos 6 caracteres';
        }
    } else if (input.name === 'confirmSenha' && input.value) {
        const senhaInput = document.getElementById('senha');
        if (senhaInput && input.value !== senhaInput.value) {
            isValid = false;
            message = 'As senhas n√£o coincidem';
        }
    }
    
    // Verificar se campo obrigat√≥rio est√° vazio
    if (input.required && !input.value.trim()) {
        isValid = false;
        message = 'Este campo √© obrigat√≥rio';
    }
    
    // Aplicar estilos visuais
    if (isValid) {
        formGroup.classList.remove('invalid');
        formGroup.classList.add('valid');
    } else {
        formGroup.classList.remove('valid');
        formGroup.classList.add('invalid');
        showFieldError(input, message);
    }
    
    return isValid;
}

// Mostrar erro no campo
function showFieldError(input, message) {
    const formGroup = input.closest('.form-group');
    let errorElement = formGroup.querySelector('.error-message');
    
    if (!errorElement) {
        errorElement = document.createElement('div');
        errorElement.className = 'error-message';
        errorElement.style.cssText = `
            color: #dc2626;
            font-size: 0.8em;
            margin-top: 4px;
        `;
        formGroup.appendChild(errorElement);
    }
    
    errorElement.textContent = message;
}

// Limpar erro do campo
function clearFieldError(input) {
    const formGroup = input.closest('.form-group');
    formGroup.classList.remove('invalid');
    
    const errorElement = formGroup.querySelector('.error-message');
    if (errorElement) {
        errorElement.textContent = '';
    }
}

// Fun√ß√£o para mostrar toast
function showToast(message, type = 'info') {
    const toast = document.createElement('div');
    toast.className = `toast ${type}`;
    toast.textContent = message;
    
    // Criar container se n√£o existir
    let container = document.getElementById('toastContainer');
    if (!container) {
        container = document.createElement('div');
        container.id = 'toastContainer';
        container.className = 'toast-container';
        container.style.cssText = `
            position: fixed;
            top: 20px;
            right: 20px;
            z-index: 10000;
        `;
        document.body.appendChild(container);
    }
    
    // Estilos do toast
    toast.style.cssText = `
        background: ${type === 'success' ? '#A7C0BE' : type === 'error' ? '#dc2626' : '#4D6772'};
        color: white;
        padding: 12px 24px;
        border-radius: 8px;
        margin-bottom: 10px;
        box-shadow: 0 4px 8px rgba(0,0,0,0.1);
        opacity: 0;
        transform: translateX(100%);
        transition: all 0.3s ease;
        max-width: 300px;
        word-wrap: break-word;
    `;
    
    container.appendChild(toast);
    
    // Mostrar toast
    setTimeout(() => {
        toast.style.opacity = '1';
        toast.style.transform = 'translateX(0)';
    }, 100);
    
    // Remover toast ap√≥s 4 segundos
    setTimeout(() => {
        toast.style.opacity = '0';
        toast.style.transform = 'translateX(100%)';
        setTimeout(() => {
            if (container.contains(toast)) {
                container.removeChild(toast);
            }
        }, 300);
    }, 4000);
}
